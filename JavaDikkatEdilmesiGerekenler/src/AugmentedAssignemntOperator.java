
public class AugmentedAssignemntOperator {

	public static void main(String[] args) {
		System.out.println("ARTTIRILMIÞ ATAMA ÝFADELERÝ ");
		System.out.println("");
		
		int number =5 ;
		System.out.println("Ýslemden önceki number degeri : " +number);
		number+=3;
		System.out.println("Atama iþlemi yapildiktan sonraki deger (5+3) : " +number);
		System.out.println("");
		
		int num =8;
		System.out.println("Ýslemden önceki num degeri : " +num);
		num-=2;
		System.out.println("Atama iþlemi yapildiktan sonraki deger (8-2) : " +num );
		System.out.println("");
		
		int alfa=6;
		System.out.println("Ýslemden önceki alfa degeri : " +alfa);
		alfa*=3;
		System.out.println("Atama iþlemi yapildiktan sonraki deger (6*3) : " +alfa );
		System.out.println("");
		
		int sayi=10 ;
		System.out.println("Ýslemden önceki sayi degeri : " +sayi);
		sayi/=2;
		System.out.println("Atama iþlemi yapildiktan sonraki deger (10/2) : " +sayi );
		
	}

}
